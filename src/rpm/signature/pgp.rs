    mod keyring {
        use super::*;

        fn prep_keyring_signer() -> Signer {
            let _ = env_logger::try_init();
            let keyring = include_bytes!(
                "../../../tests/assets/signing_keys/v4/rpm-testkey-v4-keyring.secret"
            );
            Signer::from_asc_bytes(keyring.as_slice()).expect("keyring parsing failed")
        }

        /// Verify that `with_signing_key` updates user_id to match the certificate
        /// that owns the selected key.
        #[test]
        fn with_signing_key_updates_user_id() {
            let signer = prep_keyring_signer();
            let original_user_id = signer.user_id.clone();

            // Find a different cert in the keyring
            let other_cert = signer
                .signed_keys
                .iter()
                .find(|sk| sk.details.users.first().map(|u| u.id.id().to_vec()) != original_user_id)
                .expect("keyring should contain multiple certs with different user IDs");

            let other_user_id = other_cert.details.users.first().map(|u| u.id.id().to_vec());
            let other_fingerprint = other_cert.primary_key.fingerprint();

            let switched_signer = signer
                .with_signing_key(other_fingerprint.as_bytes())
                .expect("key selection should succeed");

            assert_eq!(
                switched_signer.user_id, other_user_id,
                "user_id should match the selected key's certificate"
            );
            assert_ne!(
                switched_signer.user_id, original_user_id,
                "user_id should have changed from the original"
            );
        }

        /// Verify that the SignersUserID subpacket in the signature matches the certificate
        /// selected via `with_signing_key`.
        #[test]
        fn with_signing_key_signature_contains_correct_user_id() {
            const PASSPHRASE: &str = "thisisN0Tasecuredpassphrase";
            let signer = prep_keyring_signer();

            for signed_key in &signer.signed_keys {
                let expected_uid = signed_key.details.users.first().map(|u| u.id.id().to_vec());
                let fpr = signed_key.primary_key.fingerprint();

                let selected_signer = signer
                    .clone()
                    .with_signing_key(fpr.as_bytes())
                    .expect("key selection should succeed")
                    .with_key_passphrase(PASSPHRASE);

                let signature = selected_signer
                    .sign(Cursor::new(TEST_DATA), TEST_TIMESTAMP)
                    .expect("signing should succeed");

                let parsed = Verifier::parse_signature(&signature).expect("should parse");
                let config = parsed.config().expect("should have config");
                let sig_uid = config
                    .hashed_subpackets
                    .iter()
                    .find_map(|sp| match &sp.data {
                        SubpacketData::SignersUserID(uid) => Some(uid.as_ref().to_vec()),
                        _ => None,
                    });

                assert_eq!(
                    sig_uid, expected_uid,
                    "signature should contain the user ID from the selected key's certificate"
                );
            }
        }
    }
